def build_reply_keyboard(
    *,
    cancel: bool = True,
    skip: bool = False,
    extras: Optional[Tuple[str, ...]] = None,
    placeholder: Optional[str] = None,
) -> ReplyKeyboardMarkup:
    rows = []
    first_row = []
    if cancel:
        first_row.append(KeyboardButton(text="–û—Ç–º–µ–Ω–∞"))
    if skip:
        first_row.append(KeyboardButton(text="–ü—Ä–æ–ø—É—Å—Ç–∏—Ç—å"))
    if first_row:
        rows.append(first_row)
    if extras:
        for text in extras:
            rows.append([KeyboardButton(text=text)])
    return ReplyKeyboardMarkup(
        keyboard=rows,
        resize_keyboard=True,
        one_time_keyboard=True,
        input_field_placeholder=placeholder,
    )


async def abort_flow(message: types.Message, state: FSMContext, text: str):
    await message.answer(text, reply_markup=ReplyKeyboardRemove())
    await state.clear()
    await send_admin_menu(message)


